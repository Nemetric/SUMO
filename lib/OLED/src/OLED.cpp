void OLED::init()
{
	pinMode(16, OUTPUT);
	digitalWrite(16, LOW); // set GPIO16 low to reset OLED
	delay(50);
	digitalWrite(16, HIGH); // while OLED is running, must set GPIO16 to high

	Wire.begin(4, 15);
	oled.init();		 // Initialze SSD1306 OLED display
	//oled.setFont(font5x7);            // Set font type (default 8x8)
	//oled.setFont(font);
	oled.clearDisplay(); // Clear screen
}

void OLED::writeYETIBOTICS()
{
	oled.clearDisplay();			// Clear screen
	oled.drawBitmap(YETIBOTICS, 1024); // 1024 pixels for logo
}
void OLED::writeNEMETRIC()
{
	oled.clearDisplay();			// Clear screen
	oled.drawBitmap(NEMETRIC, 1024); // 1024 pixels for logo
}

void OLED::Reset()
{
 oled.clearDisplay();              // Clear screen
  oled.setTextXY(0,0);              // Set cursor position, start of line 0

}

void OLED::WriteString(String txt)
{
	oled.setTextXY(0,0);   
  oled.putString(txt);

}

void OLED::WriteString(String txt, int x, int y)
{
	oled.setTextXY(y,x);   
  oled.putString(txt);

}


void OLED::WriteInt(int txt)
{
	oled.setTextXY(0,0);   
	oled.putString("            ");
	oled.setTextXY(0,0);  
  oled.putNumber(txt);

}
